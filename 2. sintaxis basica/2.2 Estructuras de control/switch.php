<?php
//estructura switch permite decidir entre multiples opciones
$i=3;
switch ($i) {
    case 0:
        echo "i es igual a 0";
        break;
    case 1:
        echo "i es igual a 1";
        break;
    case 2:
        echo "i es igual a 2";
        break;
    default:
        echo "ERROR DE ENTRADA";
}
?>